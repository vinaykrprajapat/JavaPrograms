class HexaDecimalRep {
public static void main(String ar[]) {
	int a=0x5;	  // value of a is equivalent to decimal 5
	                 int b=0XF;	  // value of b is equivalent to decimal 15
	int c= 0xd;	  // value of c is equivalent to decimal 13
	System.out.println ("Decimal five= "+a);
	System.out.println ("Decimal fifteen= "+b);
	System.out.println ("Decimal thirteen="+c);
	}
}
