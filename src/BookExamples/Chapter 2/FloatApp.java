/* Calculating the arithmetic operations on the two float type numbers */
class FloatApp
{
 	public static void main(String args[])
 	{
 		float p = 5.5f;
 		float q = 3.3f;
 		System.out.println("The first number is:" + p);
 		System.out.println("The second number is:" + q);
 		float r = p + q;
 		System.out.println("The addition is:" + r);
 		r = p - q;
 		System.out.println("The subtraction is:" + r);
 		r = p * q;
 		System.out.println("The multiplication is:" + r);
 		r = p / q;
 		System.out.println("The division is:" + r);
 		r = p % q;
 		System.out.println("The remainder is:" + r);
 	}
}
