/* Calculating the remainder left after dividing the two numbers */

class ModuloApp
{
 	public static void main(String args[])
 	{
 		int p = 5;
 		int q = 3;
 		System.out.println("The first number is:" + p);
 		System.out.println("The second number is:" + q);
 		int r = p % q;
 		System.out.println("The remainder is:" + r);
 	}
}
